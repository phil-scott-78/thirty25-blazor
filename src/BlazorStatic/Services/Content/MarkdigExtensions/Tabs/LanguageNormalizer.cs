        if (string.IsNullOrWhiteSpace(input))
            return "Text";

        var colonLocation = input.IndexOf(':');
        if (colonLocation > -1)
        {
            input = input[..colonLocation];
        }
        
        return LanguageMap.GetValueOrDefault(input, input);
